package kaganinjava;
import java.util.*;

public class ikinciproje {

	public static void main(String[] args) {
		
		int a = 2;
		int b = 3;
		int c = 4;
		int d = 5;
		int e = 6;
		
		int dongu = 0;
		
		while (dongu<4) {
			a = a + 1;
			b = b + 2;
			c = c + 3;
			d = d + 4;
			e = e + 5;
			
			dongu = dongu + 1;
			
			System.out.println(a + " " 
							 + b + " " +
							 + c + " " + 
							 + d + " " +
							 + e + " ");
		}
		

	}

}
